from pathlib import Path
import csv
import math
import matplotlib.pyplot as plt


OUT = Path.home() / "bc" / "petsc-benchmarks-rtol1e10"

MAIN_CSV = OUT / "final" / "tables" / "petsc_main_results.csv"
TABLE_TEX = OUT / "final" / "tables" / "table_petsc_main_results.tex"

FIG_DIR = OUT / "final" / "figures"
RESIDUAL_DIR = OUT / "figures"

TOL = 1.0e-10


def read_csv_dict(path):
    with path.open(newline="") as f:
        return list(csv.DictReader(f))


def fmt_int(x):
    return str(int(float(x)))


def fmt_one_decimal(x):
    return f"{float(x):.1f}"


def fmt_time(x):
    return f"{float(x):.3e}"


def write_main_latex_table():
    rows = read_csv_dict(MAIN_CSV)

    lines = []
    lines.append("% Auto-generated by export_petsc_thesis_outputs.py")
    lines.append("\\begin{table}[!htbp]")
    lines.append("    \\centering")
    lines.append("    \\small")
    lines.append("    \\setlength{\\tabcolsep}{4pt}")
    lines.append("    \\resizebox{\\textwidth}{!}{%")
    lines.append("    \\begin{tabular}{c | r r r | r r r | r}")
    lines.append("        \\toprule")
    lines.append("        \\multicolumn{1}{c|}{} & \\multicolumn{3}{c|}{FETI-DP} & \\multicolumn{3}{c|}{BDDC} & \\multicolumn{1}{c}{Total run} \\\\")
    lines.append("        Case & $n_{\\lambda}$ & it & $\\lambda_{\\max}$ & $n_{\\mathrm{phys}}$ & it & $\\lambda_{\\max}$ & time [s] \\\\")
    lines.append("        \\midrule")

    for r in rows:
        case_id = r["case_id"]
        feti_dofs = fmt_int(r["feti_dofs"])
        feti_iters = fmt_int(r["feti_iters"])
        feti_lmax = fmt_one_decimal(r["feti_lambda_max"])

        bddc_dofs = fmt_int(r["bddc_dofs"])
        bddc_iters = fmt_int(r["bddc_iters"])
        bddc_lmax = fmt_one_decimal(r["bddc_lambda_max"])

        tmed = fmt_time(r["median_total_time_sec"])

        lines.append(
            f"        {case_id} & {feti_dofs} & {feti_iters} & {feti_lmax} "
            f"& {bddc_dofs} & {bddc_iters} & {bddc_lmax} & {tmed} \\\\"
        )

    lines.append("        \\bottomrule")
    lines.append("    \\end{tabular}%")
    lines.append("    }")
    lines.append("    \\caption{Summary of PETSc benchmark results for FETI-DP and BDDC with the relative tolerance $10^{-10}$. The quantity $n_{\\lambda}$ denotes the dimension of the FETI-DP multiplier problem, while $n_{\\mathrm{phys}}$ denotes the number of active physical degrees of freedom in the BDDC branch. In all cases PETSc reported $\\lambda_{\\min}=1.0$, so the reported value $\\lambda_{\\max}$ is also the corresponding condition-number indicator. The time is the median total wall-clock time of the complete PETSc run over three repetitions.}")
    lines.append("    \\label{tab:ch6-petsc-main-results}")
    lines.append("\\end{table}")

    TABLE_TEX.parent.mkdir(parents=True, exist_ok=True)
    TABLE_TEX.write_text("\n".join(lines) + "\n")


def read_residual_csv(path):
    rows = []
    with path.open(newline="") as f:
        reader = csv.DictReader(f)
        for r in reader:
            rows.append(r)
    return rows


def as_float_or_none(value):
    if value is None or value == "":
        return None
    return float(value)


def as_int_or_none(value):
    if value is None or value == "":
        return None
    return int(value)


def plot_residual_case(case_id):
    path = RESIDUAL_DIR / f"{case_id}_residuals.csv"
    rows = read_residual_csv(path)

    bddc_x, bddc_y = [], []
    feti_x, feti_y = [], []

    for r in rows:
        ib = as_int_or_none(r.get("iter_bddc"))
        rb = as_float_or_none(r.get("bddc_relres"))
        if ib is not None and rb is not None:
            bddc_x.append(ib)
            bddc_y.append(rb)

        ife = as_int_or_none(r.get("iter_fetidp"))
        rfe = as_float_or_none(r.get("fetidp_relres"))
        if ife is not None and rfe is not None:
            feti_x.append(ife)
            feti_y.append(rfe)

    fig, ax = plt.subplots(figsize=(6.8, 4.4))

    # Main curves in the same simple style as thesis residual figures.
    ax.semilogy(
        feti_x, feti_y,
        marker="o",
        linewidth=1.6,
        markersize=4,
        label="FETI-DP"
    )
    ax.semilogy(
        bddc_x, bddc_y,
        marker="s",
        linewidth=1.6,
        markersize=4,
        label="BDDC"
    )

    # # Red dashed horizontal tolerance / stopping-threshold line.
    #xmax = max(max(feti_x), max(bddc_x))
    # ax.semilogy(
    #     [0, xmax],
    #     [TOL, TOL],
    #     "r--",
    #     linewidth=1.4,
    #     label=r"Relative tolerance $10^{-10}$"
    # )

    ax.set_xlabel("Iteration")
    ax.set_ylabel(r"True relative residual $\|r_k\|/\|b\|$")
    ax.grid(True, which="both", linestyle=":", linewidth=0.6)
    #ax.set_xlim(left=0, right=xmax)

    ymin = min(min(feti_y), min(bddc_y), TOL)
    ymax = max(max(feti_y), max(bddc_y))
    ax.set_ylim(
        bottom=10 ** (math.floor(math.log10(ymin)) - 1),
        top=10 ** (math.ceil(math.log10(ymax)) + 1)
    )

    ax.legend(loc="best", frameon=True)

    # No title inside the graph; thesis figures use external captions.
    fig.tight_layout()

    FIG_DIR.mkdir(parents=True, exist_ok=True)
    fig.savefig(FIG_DIR / f"fig_petsc_residual_{case_id}.pdf")
    fig.savefig(FIG_DIR / f"fig_petsc_residual_{case_id}.png", dpi=300)
    plt.close(fig)


def main():
    write_main_latex_table()

    plot_residual_case("CM")
    plot_residual_case("CL")

    print(f"Wrote LaTeX table: {TABLE_TEX}")
    print(f"Wrote figures to: {FIG_DIR}")


if __name__ == "__main__":
    main()
